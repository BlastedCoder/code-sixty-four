CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- ------------------------------------------
-- 2. TABLE CREATION
-- ------------------------------------------

CREATE TABLE public.profiles (
  id UUID NOT NULL,
  display_name TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  CONSTRAINT profiles_pkey PRIMARY KEY (id),
  CONSTRAINT profiles_id_fkey FOREIGN KEY (id) REFERENCES auth.users (id) ON DELETE CASCADE
) TABLESPACE pg_default;

CREATE TABLE public.leagues (
  id UUID NOT NULL DEFAULT extensions.uuid_generate_v4(),
  name TEXT NOT NULL,
  created_by UUID NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  invite_code TEXT UNIQUE,
  status TEXT DEFAULT 'pre_draft'::TEXT,
  current_pick INTEGER DEFAULT 1,
  max_members INTEGER DEFAULT 8,
  season_year INTEGER DEFAULT 2026,
  member_count INTEGER DEFAULT 0,
  CONSTRAINT leagues_pkey PRIMARY KEY (id),
  CONSTRAINT leagues_created_by_fkey FOREIGN KEY (created_by) REFERENCES profiles (id),
  CONSTRAINT leagues_status_check CHECK (status IN ('pre_draft', 'drafting', 'in_progress', 'completed'))
) TABLESPACE pg_default;

CREATE TABLE public.league_members (
  league_id UUID NOT NULL,
  user_id UUID NOT NULL,
  joined_at TIMESTAMPTZ DEFAULT NOW(),
  draft_position INTEGER,
  CONSTRAINT league_members_pkey PRIMARY KEY (league_id, user_id),
  CONSTRAINT league_members_league_id_fkey FOREIGN KEY (league_id) REFERENCES leagues (id) ON DELETE CASCADE,
  CONSTRAINT league_members_user_id_fkey FOREIGN KEY (user_id) REFERENCES profiles (id) ON DELETE CASCADE
) TABLESPACE pg_default;

CREATE TABLE public.teams (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name TEXT NOT NULL,
  seed INTEGER NOT NULL,
  region TEXT NOT NULL,
  wins INTEGER DEFAULT 0,
  is_eliminated BOOLEAN DEFAULT FALSE,
  season_year INTEGER DEFAULT 2026,
  CONSTRAINT teams_pkey PRIMARY KEY (id),
  CONSTRAINT teams_seed_check CHECK (seed >= 1 AND seed <= 16)
) TABLESPACE pg_default;

CREATE TABLE public.games (
  id INTEGER NOT NULL,
  round INTEGER NOT NULL,
  team1_id INTEGER,
  team2_id INTEGER,
  winner_id INTEGER,
  next_game_id INTEGER,
  team1_score INTEGER,
  team2_score INTEGER,
  is_completed BOOLEAN DEFAULT FALSE,
  season_year INTEGER DEFAULT 2026,
  CONSTRAINT games_pkey PRIMARY KEY (id),
  CONSTRAINT games_next_game_id_fkey FOREIGN KEY (next_game_id) REFERENCES games (id),
  CONSTRAINT games_team1_id_fkey FOREIGN KEY (team1_id) REFERENCES teams (id),
  CONSTRAINT games_team2_id_fkey FOREIGN KEY (team2_id) REFERENCES teams (id),
  CONSTRAINT games_winner_id_fkey FOREIGN KEY (winner_id) REFERENCES teams (id),
  CONSTRAINT games_round_check CHECK (round >= 1 AND round <= 6)
) TABLESPACE pg_default;

CREATE TABLE public.draft_picks (
  id UUID NOT NULL DEFAULT extensions.uuid_generate_v4(),
  league_id UUID NOT NULL,
  user_id UUID NOT NULL,
  team_id INTEGER NOT NULL,
  drafted_at TIMESTAMPTZ DEFAULT NOW(),
  pick_number INTEGER,
  CONSTRAINT draft_picks_pkey PRIMARY KEY (id),
  CONSTRAINT draft_picks_league_id_team_id_key UNIQUE (league_id, team_id),
  CONSTRAINT draft_picks_league_id_fkey FOREIGN KEY (league_id) REFERENCES leagues (id) ON DELETE CASCADE,
  CONSTRAINT draft_picks_team_id_fkey FOREIGN KEY (team_id) REFERENCES teams (id),
  CONSTRAINT draft_picks_user_id_fkey FOREIGN KEY (user_id) REFERENCES profiles (id) ON DELETE CASCADE
) TABLESPACE pg_default;

-- ------------------------------------------
-- 3. FUNCTIONS & TRIGGERS
-- ------------------------------------------

-- Auto-create user profile on sign up
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
BEGIN
  INSERT INTO public.profiles (id, display_name)
  VALUES (NEW.id, SPLIT_PART(NEW.email, '@', 1));
  RETURN NEW;
END;
$$;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();

-- Enforce League Member Capacity
CREATE OR REPLACE FUNCTION public.check_league_capacity() 
RETURNS TRIGGER AS $$
BEGIN
  IF (SELECT COUNT(*) FROM public.league_members WHERE league_id = NEW.league_id) >= 
     (SELECT max_members FROM public.leagues WHERE id = NEW.league_id) THEN
    RAISE EXCEPTION 'League has reached its maximum capacity';
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER enforce_max_members
  BEFORE INSERT ON public.league_members
  FOR EACH ROW EXECUTE FUNCTION public.check_league_capacity();

-- Keep League Member Count Synced
CREATE OR REPLACE FUNCTION public.update_league_member_count()
RETURNS TRIGGER AS $$
BEGIN
  IF TG_OP = 'INSERT' THEN
    UPDATE public.leagues SET member_count = member_count + 1 WHERE id = NEW.league_id;
    RETURN NEW;
  ELSIF TG_OP = 'DELETE' THEN
    UPDATE public.leagues SET member_count = member_count - 1 WHERE id = OLD.league_id;
    RETURN OLD;
  END IF;
  RETURN NULL;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_update_league_member_count
  AFTER INSERT OR DELETE ON public.league_members
  FOR EACH ROW EXECUTE FUNCTION public.update_league_member_count();

