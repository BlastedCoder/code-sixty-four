-- Populate 64 Tournament Teams
DO $$
DECLARE
    r TEXT;
    s INT;
    team_id INT := 1;
    regions TEXT[] := ARRAY['East', 'West', 'South', 'Midwest'];
BEGIN
    FOREACH r IN ARRAY regions LOOP
        FOR s IN 1..16 LOOP
            INSERT INTO public.teams (id, name, seed, region, season_year)
            VALUES (team_id, r || ' Seed ' || s, s, r, 2026);
            team_id := team_id + 1;
        END LOOP;
    END LOOP;
END $$;

-- Generate 63 Games & Round 1 Matchups
DO $$
DECLARE
    g INT;
    next_g INT;
    r INT;
    team_offset INT;
    matchups INT[] := ARRAY[1, 16, 8, 9, 5, 12, 4, 13, 6, 11, 3, 14, 7, 10, 2, 15];
BEGIN
    -- 1. Insert Round 1: Games 1-32
    FOR g IN 1..32 LOOP
        next_g := 32 + CEIL(g / 2.0);
        r := FLOOR((g - 1) / 8);
        team_offset := r * 16;
        
        INSERT INTO public.games (id, round, next_game_id, team1_id, team2_id, season_year) 
        VALUES (
            g, 1, next_g, 
            team_offset + matchups[(((g - 1) % 8 + 1) * 2) - 1], 
            team_offset + matchups[((g - 1) % 8 + 1) * 2],
            2026
        );
    END LOOP;

    -- 2. Insert Round 2: Games 33-48
    FOR g IN 33..48 LOOP
        next_g := 48 + CEIL((g - 32) / 2.0);
        INSERT INTO public.games (id, round, next_game_id, season_year) VALUES (g, 2, next_g, 2026);
    END LOOP;

    -- 3. Insert Round 3 (Sweet 16): Games 49-56
    FOR g IN 49..56 LOOP
        next_g := 56 + CEIL((g - 48) / 2.0);
        INSERT INTO public.games (id, round, next_game_id, season_year) VALUES (g, 3, next_g, 2026);
    END LOOP;

    -- 4. Insert Round 4 (Elite 8): Games 57-60
    FOR g IN 57..60 LOOP
        next_g := 60 + CEIL((g - 56) / 2.0);
        INSERT INTO public.games (id, round, next_game_id, season_year) VALUES (g, 4, next_g, 2026);
    END LOOP;

    -- 5. Insert Round 5 (Final 4): Games 61-62
    FOR g IN 61..62 LOOP
        INSERT INTO public.games (id, round, next_game_id, season_year) VALUES (g, 5, 63, 2026);
    END LOOP;

    -- 6. Insert Round 6 (Championship): Game 63
    INSERT INTO public.games (id, round, next_game_id, season_year) VALUES (63, 6, NULL, 2026);
END $$;